Object.keys(styles).forEach(styleKey => {
    const dashName = camelCaseToDash(styleKey)
    const splitDashName = dashName.split("-")
    let shortName = ""

    if (splitDashName.length === 1) {
        let tempShortName = splitDashName[0]
        
        if (typeof nameTranslations?.[tempShortName] !== "undefined") {
            shortName = nameTranslations?.[tempShortName] + shortNameSeparator
        } else {
            shortName = tempShortName + shortNameSeparator
        }
    } else if (splitDashName.length > 1) {
        let finalShortName = ""
        splitDashName.forEach(name => {
            if (typeof nameTranslations?.[name] !== "undefined") {
                finalShortName = finalShortName + nameTranslations?.[name] + shortNameSeparator
            } else {
                finalShortName = finalShortName + name[0] + shortNameSeparator
            }
        })
        shortName = finalShortName
    }

    stylesDictionary = [...stylesDictionary, {
        shortName: shortName,
        camelName: styleKey,
        dashName: dashName
    }]
})


